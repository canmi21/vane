/// Per-Store state threaded through every host function call.
struct HostState {
	args: String,
	fetch_backend: Arc<dyn HttpFetchBackend>,
	/// Plugin identity. Surfaced to host fns as metric label / log
	/// field so operator-side observability can distinguish emissions
	/// across modules + exports.
	#[allow(dead_code, reason = "consumed by metric / http-fetch host fns in the next commits")]
	module_id: Arc<str>,
	#[allow(dead_code, reason = "consumed by metric / http-fetch host fns in the next commits")]
	metadata_name: Arc<str>,
	#[allow(dead_code, reason = "consumed by metric / http-fetch host fns in the next commits")]
	export_name: Arc<str>,
	/// Operator-owned http-fetch policy snapshot. Cloned per-rental
	/// from a single `Arc<PluginHttpPolicy>` so every host fn call
	/// reads the same view; reload swaps the runtime, which rebuilds
	/// every state with whatever the new policy table dictates.
	#[allow(dead_code, reason = "consumed by metric / http-fetch host fns in the next commits")]
	policy: Arc<PluginHttpPolicy>,
	/// Shared cardinality registry for metric emissions. One per
	/// `WasmtimeRuntime`; every plugin's host state holds the same
	/// `Arc`.
	#[allow(dead_code, reason = "consumed by metric host fn in the next commit")]
	cardinality: Arc<CardinalityRegistry>,
	/// Captures the last value returned by `get_args` during an invocation.
	/// Used by `StatefulPoolHandle::last_args_received` in tests to verify
	/// that the fixture plugin actually called host.get-args and received the
	/// bound args string.
	#[cfg(test)]
	args_received: Option<String>,
}

impl HostState {
	#[allow(clippy::too_many_arguments, reason = "every field is a per-plugin context handle")]
	fn new(
		args: String,
		fetch_backend: Arc<dyn HttpFetchBackend>,
		module_id: Arc<str>,
		metadata_name: Arc<str>,
		export_name: Arc<str>,
		policy: Arc<PluginHttpPolicy>,
		cardinality: Arc<CardinalityRegistry>,
	) -> Self {
		Self {
			args,
			fetch_backend,
			module_id,
			metadata_name,
			export_name,
			policy,
			cardinality,
			#[cfg(test)]
			args_received: None,
		}
	}
}

// vane:plugin/types has no functions; the generated Host trait is empty.
impl vane::plugin::types::Host for HostState {}

// Implement the host import trait generated by `bindgen!`.
// Native async fn is used (RPITIT, no #[async_trait]).
impl vane::host::host::Host for HostState {
	async fn get_args(&mut self) -> wasmtime::Result<String> {
		let result = self.args.clone();
		#[cfg(test)]
		{
			self.args_received = Some(result.clone());
		}
		Ok(result)
	}

	async fn log(
		&mut self,
		level: vane::host::host::LogLevel,
		message: String,
		fields: Vec<vane::host::host::LogField>,
	) -> wasmtime::Result<()> {
		use vane::host::host::LogLevel;
		let kv: String = fields.iter().fold(String::new(), |mut s, f| {
			use std::fmt::Write as _;
			let _ = write!(s, " {}={}", f.key, f.value);
			s
		});
		match level {
			LogLevel::Trace => trace!(plugin = true, "{message}{kv}"),
			LogLevel::Debug => tracing::debug!(plugin = true, "{message}{kv}"),
			LogLevel::Info => tracing::info!(plugin = true, "{message}{kv}"),
			LogLevel::Warn => warn!(plugin = true, "{message}{kv}"),
			LogLevel::Error => tracing::error!(plugin = true, "{message}{kv}"),
		}
		Ok(())
	}

	async fn now_unix_ms(&mut self) -> wasmtime::Result<u64> {
		let d = SystemTime::now().duration_since(UNIX_EPOCH).unwrap_or_default();
		Ok(d.as_secs() * 1000 + u64::from(d.subsec_millis()))
	}

	async fn random(&mut self, buf_len: u32) -> wasmtime::Result<Vec<u8>> {
		let mut buf = vec![0u8; buf_len as usize];
		rand::rng().fill_bytes(&mut buf);
		Ok(buf)
	}

	async fn metric_counter(
		&mut self,
		name: String,
		delta: u64,
		labels: Vec<vane::host::host::MetricLabel>,
	) -> wasmtime::Result<()> {
		let user_labels: Vec<(String, String)> = labels.into_iter().map(|l| (l.key, l.value)).collect();
		metric_counter_core(self, &name, delta, &user_labels)
	}

	async fn metric_gauge(
		&mut self,
		name: String,
		value: i64,
		labels: Vec<vane::host::host::MetricLabel>,
	) -> wasmtime::Result<()> {
		let user_labels: Vec<(String, String)> = labels.into_iter().map(|l| (l.key, l.value)).collect();
		metric_gauge_core(self, &name, value, &user_labels)
	}

	async fn http_fetch(
		&mut self,
		req: vane::host::host::HttpFetchRequest,
	) -> wasmtime::Result<Result<vane::host::host::HttpFetchResponse, vane::host::host::NetError>> {
		let vane_req = HttpFetchRequest {
			method: req.method,
			url: req.url,
			headers: req.headers,
			body: req.body,
			timeout_ms: req.timeout_ms,
			follow_redirects: req.follow_redirects,
			verify_tls: req.verify_tls,
		};
		match http_fetch_core(self, vane_req).await? {
			Ok(resp) => Ok(Ok(vane::host::host::HttpFetchResponse {
				status: resp.status,
				headers: resp.headers,
				body: resp.body,
			})),
			Err(e) => Ok(Err(map_fetch_error(e))),
		}
	}
}

