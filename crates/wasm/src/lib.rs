/// A pre-allocated pool of warm stateful WASM instances for a single export.
///
/// Instances are checked out (popped), used, and returned (pushed). If the
/// pool is empty, `invoke_l4_peek` returns `PluginError::Exhausted` immediately
/// without blocking.
pub struct StatefulPoolHandle {
	/// Current `Component` for this pool's module. Held inside
	/// [`ArcSwap`] so a hot-reload swap publishes the new component
	/// atomically; lazy-built instances after a generation bump pick
	/// up the new component on their next checkout.
	component: arc_swap::ArcSwap<Component>,
	engine: Engine,
	fetch_backend: Arc<dyn HttpFetchBackend>,
	pub args_json: String,
	instances: Mutex<Vec<StatefulInstance>>,
	pub capacity: usize,
	/// Identity used by `WasmtimeRuntime::pool_snapshot` to label this
	/// pool in mgmt verb output. Stored on the handle (rather than as
	/// a side map keyed off `Arc::as_ptr`) so the snapshot can echo
	/// the operator's module + export without a second registry.
	pub module_id: String,
	pub export_name: String,
	/// Cumulative checkout success count. Ticks once per `instances.pop()`
	/// that returns `Some(_)`; surfaced via `WasmPoolSnapshot`.
	pub total_allocations: Arc<std::sync::atomic::AtomicU64>,
	/// Cumulative checkout failure count — every `PluginError::Exhausted`
	/// path increments this.
	pub failures: Arc<std::sync::atomic::AtomicU64>,
	/// Reload epoch. Pre-built and lazily-constructed instances are
	/// tagged with the value of this counter at creation time; on
	/// reload, [`Self::bump_generation`] increments it and clears
	/// pre-built instances so subsequent checkouts go through the
	/// lazy-build path against the freshly-swapped `Component`.
	/// In-flight instances finish naturally; their return path drops
	/// mismatched-generation entries instead of pushing them back.
	pub generation: std::sync::atomic::AtomicU64,
	/// Number of instances currently checked out. Used by the
	/// lazy-build path to enforce `capacity` after a generation bump
	/// drained the pre-built buffer.
	pub in_flight: std::sync::atomic::AtomicUsize,
	/// `Arc<str>` snapshot of `module_id` for `HostState` construction
	/// in the lazy-build path (`HostState`'s API is `Arc<str>`-typed).
	module_id_arc: Arc<str>,
	/// `Arc<str>` snapshot of `export_name` for the same reason.
	export_name_arc: Arc<str>,
	/// `metadata.name` snapshot at create time. Used as a label in
	/// `HostState`'s cardinality bookkeeping. Reload may change the
	/// underlying `metadata.name` field but pools carry the old label
	/// until the next reload reaches them — the label is observability
	/// only, never load-bearing.
	metadata_name: Arc<str>,
	/// Operator-owned http-fetch policy snapshot. Cached at create
	/// time; later `set_policy` calls update the runtime's table but
	/// existing pools see the snapshot — pool policy refresh is a
	/// reload-time event (see follow-up flag in spec).
	policy: Arc<PluginHttpPolicy>,
	/// Shared cardinality registry — `Arc`-stable across reloads, so
	/// we just clone the pointer at create time.
	cardinality: Arc<CardinalityRegistry>,
}

struct StatefulInstance {
	store: Store<HostState>,
	plugin: invoke_l4peek::PluginL4PeekInvoke,
	/// Generation this instance was built against. Compared on
	/// checkout / return so old-generation instances drop after a
	/// reload bump rather than re-pooling.
	generation: u64,
}

impl StatefulPoolHandle {
	/// Invoke the l4-peek handler using a pooled stateful instance.
	///
	/// Checkout is non-blocking: if no instance is available, returns
	/// `PluginError::Exhausted` immediately.
	///
	/// # Errors
	///
	/// Returns `PluginError::Exhausted` if no instance is available.
	/// Returns `PluginError::Trap` if the guest traps or the epoch deadline fires.
	/// Returns `PluginError::Plugin` if the guest returns an in-band error.
	///
	/// # Panics
	///
	/// Panics if the internal instance mutex is poisoned.
	pub async fn invoke_l4_peek(
		&self,
		export_name: &str,
		input: L4PeekInput,
	) -> Result<L4PeekDecision, PluginError> {
		use std::sync::atomic::Ordering;

		let cur_gen = self.generation.load(Ordering::Acquire);
		let mut instance = self.checkout(cur_gen, export_name).await?;
		// `checkout` already reserved the `in_flight` slot atomically
		// (the cap check + reservation live there to close a TOCTOU
		// window between observing in_flight and racing concurrent
		// lazy builds). Just record the cumulative success counter.
		self.total_allocations.fetch_add(1, Ordering::Relaxed);

		instance.store.set_epoch_deadline(10);
		let wit_input = lower_input(input);
		let result = instance.plugin.vane_plugin_handler_l4_peek().call_handle(
			&mut instance.store,
			export_name,
			&wit_input,
		);

		let outcome = match result {
			Ok(Ok(d)) => Ok(lift_decision(d)),
			Ok(Err(pe)) => Err(lift_plugin_error(pe)),
			Err(e) => Err(PluginError::Trap(e.to_string())),
		};

		// Return path: drop in_flight unconditionally, then re-pool only
		// if the instance's generation still matches the current epoch.
		// Mismatch means a reload bumped generation while this instance
		// was checked out — drop it so the pool stays at-most one
		// generation behind reload.
		self.in_flight.fetch_sub(1, Ordering::AcqRel);
		let cur_gen_after = self.generation.load(Ordering::Acquire);
		if instance.generation == cur_gen_after {
			let mut guard = self.instances.lock().unwrap();
			guard.push(instance);
		}

		outcome
	}

	/// Acquire a current-generation instance: pop from the pre-built
	/// buffer if one is available with the right generation, otherwise
	/// lazily build a fresh one against the current `Component`. Old-
	/// generation instances popped from the buffer are dropped silently.
	///
	/// Capacity is enforced by reserving an `in_flight` slot atomically
	/// **before** observing the buffer or calling `lazy_build_instance`.
	/// A `fetch_add(1)` whose previous value already exceeds `capacity`
	/// rolls back via `fetch_sub(1)` and returns `Exhausted`, so two
	/// concurrent checkouts can never both pass the cap check and both
	/// proceed to a lazy build. Without this protocol, `load` + `fetch_add`
	/// done separately would let a burst bump `in_flight` past `capacity`.
	async fn checkout(
		&self,
		cur_gen: u64,
		export_name: &str,
	) -> Result<StatefulInstance, PluginError> {
		use std::sync::atomic::Ordering;

		// Atomic reservation — single fetch_add covers the entire
		// checkout regardless of which arm (pre-built pop vs lazy
		// build) ends up serving the slot. Old-generation pops drop
		// and retry without releasing the reservation: dropping them
		// frees no real capacity (they were not in_flight to begin
		// with), and the next loop iteration either pops a current-
		// generation instance or falls through to the lazy build for
		// the same reserved slot.
		let prev = self.in_flight.fetch_add(1, Ordering::AcqRel);
		if prev >= self.capacity {
			self.in_flight.fetch_sub(1, Ordering::AcqRel);
			self.failures.fetch_add(1, Ordering::Relaxed);
			return Err(PluginError::Exhausted);
		}

		loop {
			let popped = {
				let mut guard = self.instances.lock().unwrap();
				guard.pop()
			};
			match popped {
				Some(inst) if inst.generation == cur_gen => return Ok(inst),
				Some(_old_gen) => {
					// Drop the old-generation instance and loop — the
					// pre-built buffer may still hold current-generation
					// entries below it.
				}
				None => {
					return match self.lazy_build_instance(cur_gen, export_name).await {
						Ok(inst) => Ok(inst),
						Err(e) => {
							// Build failed — release the reserved slot
							// so a subsequent checkout can use it.
							self.in_flight.fetch_sub(1, Ordering::AcqRel);
							Err(PluginError::Trap(e.to_string()))
						}
					};
				}
			}
		}
	}

	/// Build one new `StatefulInstance` against the current
	/// `Component`. Used only by [`Self::checkout`]'s lazy-build path
	/// after a generation bump drained the pre-built buffer.
	async fn lazy_build_instance(
		&self,
		generation: u64,
		_export_name: &str,
	) -> Result<StatefulInstance, Error> {
		// Linker setup is rebuilt per lazy build rather than cached on
		// the handle; `Linker<HostState>` is `!Sync` and would break
		// `Arc<StatefulPoolHandle>: Sync`. The cost is one host-fn
		// `add_to_linker` call per checkout that misses the pre-built
		// buffer — negligible compared to `instantiate_async`.
		let mut linker = Linker::<HostState>::new(&self.engine);
		invoke_l4peek::PluginL4PeekInvoke::add_to_linker::<HostState, HasSelf<HostState>>(
			&mut linker,
			|x| x,
		)
		.map_err(|e| Error::internal(format!("stateful lazy linker: {e}")))?;

		let component = self.component.load_full();
		let host_state = HostState::new(
			self.args_json.clone(),
			Arc::clone(&self.fetch_backend),
			Arc::clone(&self.module_id_arc),
			Arc::clone(&self.metadata_name),
			Arc::clone(&self.export_name_arc),
			Arc::clone(&self.policy),
			Arc::clone(&self.cardinality),
		);
		let mut store = Store::new(&self.engine, host_state);
		store.set_epoch_deadline(1000);
		let plugin =
			invoke_l4peek::PluginL4PeekInvoke::instantiate_async(&mut store, &component, &linker)
				.await
				.map_err(|e| Error::internal(format!("stateful lazy instantiate: {e}")))?;
		Ok(StatefulInstance { store, plugin, generation })
	}

	/// Replace this pool's `Component` with `new_component` and bump
	/// the generation epoch. Pre-built old-generation instances are
	/// drained eagerly; in-flight instances finish naturally and drop
	/// at return time. Spec § _Hot reload_ — pool ownership stays
	/// stable across reload, only the underlying `Component` changes.
	///
	/// # Panics
	///
	/// Panics if the internal `instances` mutex is poisoned.
	pub fn swap_component_and_bump(&self, new_component: Arc<Component>) {
		use std::sync::atomic::Ordering;
		self.component.store(new_component);
		self.generation.fetch_add(1, Ordering::AcqRel);
		self.instances.lock().unwrap().clear();
	}

	/// Returns the args string that the plugin received via `host.get-args` on
	/// the most recently returned instance, or `None` if `get-args` was never
	/// called during that invocation.
	///
	/// Only available in test builds; intended for use in
	/// `host_get_args_returns_bound_args_to_plugin` and similar.
	///
	/// # Panics
	///
	/// Panics if the instances mutex is poisoned.
	#[cfg(test)]
	pub fn last_args_received(&self) -> Option<String> {
		self.instances.lock().unwrap().last().and_then(|i| i.store.data().args_received.clone())
	}
}

/// Outcome of a single [`WasmtimeRuntime::reload_component`] call.
/// Drives the daemon's reload pipeline — `Unchanged` short-circuits,
/// `MetadataUnchanged` swaps the component without touching the
/// graph, `MetadataChanged` triggers a full graph recompile.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum ReloadComponentOutcome {
	/// The on-disk bytes hash matches the last successfully-loaded
	/// hash for this path; nothing to do.
	Unchanged,
	/// Bytes changed but the new metadata is structurally compatible
	/// with the cached metadata (same exports, kinds, `stateless`,
	/// `needs_body`, `inspects`). Pool generation is bumped so existing
	/// stateful pools pick up the new component on the next
	/// checkout; rules referencing this module need no recompile.
	MetadataUnchanged,
	/// Bytes changed and the new metadata differs in a graph-relevant
	/// way (export added / removed / kind / `stateless` / `needs_body`
	/// / `inspects`). The caller must recompile + relink the flow graph
	/// so `MiddlewareInst::Wasm` carries the new metadata `Arc`.
	MetadataChanged,
}

pub struct WasmtimeRuntime {
	engine: Engine,
	fetch_backend: Arc<dyn HttpFetchBackend>,
	epoch_abort: tokio::task::AbortHandle,
	invoke_linker: Linker<HostState>,
	invoke_l4bytes_linker: Linker<HostState>,
	invoke_l7request_linker: Linker<HostState>,
	invoke_l7response_linker: Linker<HostState>,
	components: RwLock<HashMap<String, Arc<Component>>>,
	metadata: RwLock<HashMap<String, Arc<PluginMetadata>>>,
	/// Per-path SHA-256 of the last successfully-loaded `.wasm` bytes.
	/// `reload_component` consults this to short-circuit on identical
	/// content — operators saving the same file (touch / cp -p) get
	/// `Unchanged` rather than a redundant compile + swap.
	module_hashes: RwLock<HashMap<String, [u8; 32]>>,
	stateless_pools: RwLock<HashMap<StatelessKey, Arc<StatelessPool>>>,
	/// Weak-ref registry of every live stateful pool returned by
	/// [`WasmtimeRuntime::create_stateful_pool`]. Holding `Weak` keeps
	/// the runtime out of the pool-handle ownership graph: when a flow
	/// graph reload drops the last `Arc`, the weak entry naturally
	/// invalidates and the next snapshot prunes it.
	stateful_pools: RwLock<Vec<Weak<StatefulPoolHandle>>>,
	/// Operator-owned http-fetch policy per loaded module. The
	/// daemon registers an entry via [`WasmtimeRuntime::set_policy`]
	/// after [`WasmtimeRuntime::load_component`]; lookups during
	/// `invoke_*` fall back to [`PluginHttpPolicy::default`]
	/// (deny-all) when no operator policy is registered.
	policies: RwLock<HashMap<String, Arc<PluginHttpPolicy>>>,
	/// Shared metric cardinality registry — see [`CardinalityRegistry`].
	cardinality: Arc<CardinalityRegistry>,
}

impl Drop for WasmtimeRuntime {
	fn drop(&mut self) {
		self.epoch_abort.abort();
	}
}

impl WasmtimeRuntime {
	/// Construct a new runtime. Starts the 1 ms epoch ticker immediately.
	///
	/// # Errors
	///
	/// Fails if wasmtime cannot build the pooling engine (unsupported platform
	/// or configuration conflict).
	pub fn new(fetch_backend: Arc<dyn HttpFetchBackend>) -> Result<Arc<Self>, Error> {
		Self::new_with_pool_cap(fetch_backend, 32)
	}

	/// Construct a runtime with a custom `PoolingAllocator` cap.
	///
	/// Useful in tests to exercise pool-exhaustion paths with a small cap.
	///
	/// # Errors
	///
	/// Fails if wasmtime cannot build the pooling engine (unsupported platform
	/// or configuration conflict).
	pub fn new_with_pool_cap(
		fetch_backend: Arc<dyn HttpFetchBackend>,
		pool_cap: u32,
	) -> Result<Arc<Self>, Error> {
		let engine = build_engine(pool_cap).map_err(|e| Error::internal(e.to_string()))?;

		let ticker_engine = engine.clone();
		let handle = tokio::spawn(async move {
			let mut interval = tokio::time::interval(Duration::from_millis(1));
			interval.set_missed_tick_behavior(tokio::time::MissedTickBehavior::Skip);
			loop {
				interval.tick().await;
				ticker_engine.increment_epoch();
			}
		});
		let epoch_abort = handle.abort_handle();

		let mut invoke_linker = Linker::<HostState>::new(&engine);
		invoke_l4peek::PluginL4PeekInvoke::add_to_linker::<HostState, HasSelf<HostState>>(
			&mut invoke_linker,
			|x| x,
		)
		.map_err(|e| Error::internal(format!("invoke linker setup: {e}")))?;

		let mut invoke_l4bytes_linker = Linker::<HostState>::new(&engine);
		invoke_l4bytes::PluginL4BytesInvoke::add_to_linker::<HostState, HasSelf<HostState>>(
			&mut invoke_l4bytes_linker,
			|x| x,
		)
		.map_err(|e| Error::internal(format!("l4bytes linker setup: {e}")))?;

		let mut invoke_l7request_linker = Linker::<HostState>::new(&engine);
		invoke_l7request::PluginL7RequestInvoke::add_to_linker::<HostState, HasSelf<HostState>>(
			&mut invoke_l7request_linker,
			|x| x,
		)
		.map_err(|e| Error::internal(format!("l7request linker setup: {e}")))?;

		let mut invoke_l7response_linker = Linker::<HostState>::new(&engine);
		invoke_l7response::PluginL7ResponseInvoke::add_to_linker::<HostState, HasSelf<HostState>>(
			&mut invoke_l7response_linker,
			|x| x,
		)
		.map_err(|e| Error::internal(format!("l7response linker setup: {e}")))?;

		Ok(Arc::new(Self {
			engine,
			fetch_backend,
			epoch_abort,
			invoke_linker,
			invoke_l4bytes_linker,
			invoke_l7request_linker,
			invoke_l7response_linker,
			components: RwLock::new(HashMap::new()),
			metadata: RwLock::new(HashMap::new()),
			module_hashes: RwLock::new(HashMap::new()),
			stateless_pools: RwLock::new(HashMap::new()),
			stateful_pools: RwLock::new(Vec::new()),
			policies: RwLock::new(HashMap::new()),
			cardinality: Arc::new(CardinalityRegistry::from_env()),
		}))
	}

	/// Register the operator-owned policy for a loaded module. The
	/// daemon's wasm loader resolves a [`PluginHttpPolicy`] per file
	/// stem at boot and calls this once per module before the first
	/// `invoke_*` runs. Calling without prior registration is fine —
	/// `invoke_*` falls back to [`PluginHttpPolicy::default`].
	///
	/// # Panics
	/// Panics if the internal `policies` `RwLock` is poisoned.
	pub fn set_policy(&self, module_id: &ModuleId, policy: Arc<PluginHttpPolicy>) {
		self.policies.write().unwrap().insert(module_id.0.as_ref().to_owned(), policy);
	}

	/// Read-only accessor for a loaded module's metadata. Returns
	/// `None` when the module key is not registered. Used by the
	/// daemon's reload pipeline to fetch the post-`reload_component`
	/// metadata Arc for re-registration into the new
	/// `PluginRegistry`.
	///
	/// # Panics
	///
	/// Panics if the internal `metadata` `RwLock` is poisoned.
	#[must_use]
	pub fn metadata_for_module(&self, module_id: &ModuleId) -> Option<Arc<PluginMetadata>> {
		self.metadata.read().unwrap().get(module_id.0.as_ref()).cloned()
	}

	/// Drop a module entirely from the runtime: removes its component,
	/// metadata, hash, policy, and stateless-pool entries. Stateful
	/// pools that referenced it stop being usable for the dropped
	/// module — the daemon's reload pipeline replaces / drops them
	/// via the new `PluginRegistry`.
	///
	/// # Panics
	///
	/// Panics if any internal `RwLock` is poisoned.
	pub fn unload_module(&self, module_id: &ModuleId) {
		let key = module_id.0.as_ref();
		self.components.write().unwrap().remove(key);
		self.metadata.write().unwrap().remove(key);
		self.module_hashes.write().unwrap().remove(key);
		self.policies.write().unwrap().remove(key);
		self.stateless_pools.write().unwrap().retain(|k, _| k.module_id != key);
	}

	fn policy_for(&self, module_id: &str) -> Arc<PluginHttpPolicy> {
		self
			.policies
			.read()
			.unwrap()
			.get(module_id)
			.cloned()
			.unwrap_or_else(|| Arc::new(PluginHttpPolicy::default()))
	}

	fn metadata_name_for(&self, module_id: &str) -> Arc<str> {
		self
			.metadata
			.read()
			.unwrap()
			.get(module_id)
			.map_or_else(|| Arc::from(""), |m| Arc::from(m.name.as_str()))
	}

	/// Build a [`HostState`] for one invocation. Collects the
	/// per-module metadata + policy + shared cardinality registry +
	/// fetch backend from runtime state.
	fn build_host_state(
		&self,
		args_json: String,
		module_id_str: &str,
		export_name: &str,
	) -> HostState {
		HostState::new(
			args_json,
			Arc::clone(&self.fetch_backend),
			Arc::from(module_id_str),
			self.metadata_name_for(module_id_str),
			Arc::from(export_name),
			self.policy_for(module_id_str),
			Arc::clone(&self.cardinality),
		)
	}

	/// Pre-allocate a fixed-size pool of warm stateful instances for the named export.
	///
	/// `pool_size` is clamped to `[1, 64]`. Each instance is fully instantiated
	/// at creation time; subsequent invocations only need a store epoch reset and
	/// a function call.
	///
	/// # Errors
	///
	/// Returns an error if the module has not been loaded, or if instantiation fails.
	///
	/// # Panics
	///
	/// Panics if the internal component or metadata `RwLock` is poisoned.
	pub async fn create_stateful_pool(
		&self,
		module_id: &ModuleId,
		export_name: &str,
		args_json: &str,
		pool_size: u8,
	) -> Result<Arc<StatefulPoolHandle>, Error> {
		let pool_size = (pool_size.clamp(1, 64)) as usize;
		let key = module_id.0.as_ref().to_owned();

		let component = self
			.components
			.read()
			.unwrap()
			.get(&key)
			.cloned()
			.ok_or_else(|| Error::internal("module not loaded"))?;

		let mut linker = Linker::<HostState>::new(&self.engine);
		invoke_l4peek::PluginL4PeekInvoke::add_to_linker::<HostState, HasSelf<HostState>>(
			&mut linker,
			|x| x,
		)
		.map_err(|e| Error::internal(format!("stateful linker setup: {e}")))?;

		let mut instances = Vec::with_capacity(pool_size);
		for _ in 0..pool_size {
			let host_state = self.build_host_state(args_json.to_owned(), &key, export_name);
			let mut store = Store::new(&self.engine, host_state);
			store.set_epoch_deadline(1000);
			let plugin =
				invoke_l4peek::PluginL4PeekInvoke::instantiate_async(&mut store, &component, &linker)
					.await
					.map_err(|e| Error::internal(format!("stateful instantiate: {e}")))?;
			instances.push(StatefulInstance { store, plugin, generation: 0 });
		}

		let module_id_arc: Arc<str> = Arc::from(key.as_str());
		let export_name_arc: Arc<str> = Arc::from(export_name);
		let metadata_name = self.metadata_name_for(&key);
		let policy_snapshot = self.policy_for(&key);
		let handle = Arc::new(StatefulPoolHandle {
			component: arc_swap::ArcSwap::new(component),
			engine: self.engine.clone(),
			fetch_backend: Arc::clone(&self.fetch_backend),
			args_json: args_json.to_owned(),
			instances: Mutex::new(instances),
			capacity: pool_size,
			module_id: key,
			export_name: export_name.to_owned(),
			total_allocations: Arc::new(std::sync::atomic::AtomicU64::new(0)),
			failures: Arc::new(std::sync::atomic::AtomicU64::new(0)),
			generation: std::sync::atomic::AtomicU64::new(0),
			in_flight: std::sync::atomic::AtomicUsize::new(0),
			module_id_arc,
			export_name_arc,
			metadata_name,
			policy: policy_snapshot,
			cardinality: Arc::clone(&self.cardinality),
		});
		// Register a weak ref so `pool_snapshot` can list this pool
		// without inflating its lifetime. Stale entries are pruned in
		// `pool_snapshot` itself.
		{
			let mut registry = self.stateful_pools.write().unwrap();
			registry.push(Arc::downgrade(&handle));
		}
		Ok(handle)
	}

	/// Read-only snapshot of every pool the runtime tracks: stateful
	/// pools (registered at creation time) and stateless pools (lazy,
	/// keyed by `(module, export, args_json)`). Surfaced via the
	/// `get_pools` mgmt verb.
	///
	/// Stateless pools have no pre-warmed capacity in MVP — each
	/// invocation instantiates a fresh store on demand. The snapshot
	/// reports `capacity = 0` and `available = 0` for those entries
	/// so the wire shape stays consistent with stateful pools; richer
	/// per-stateless counters (allocation totals, failures) land with
	/// the spec-mandated counters tracked by `pool-counters`.
	///
	/// # Panics
	///
	/// Panics if the internal `stateless_pools` or `stateful_pools`
	/// `RwLock` is poisoned.
	#[must_use]
	pub fn pool_snapshot(&self) -> Vec<WasmPoolSnapshot> {
		let mut out = Vec::new();
		// Stateful pools first: prune dead weak refs while we walk.
		let live: Vec<Arc<StatefulPoolHandle>> = {
			let mut registry = self.stateful_pools.write().unwrap();
			registry.retain(|w| w.strong_count() > 0);
			registry.iter().filter_map(Weak::upgrade).collect()
		};
		for pool in live {
			let available = pool.instances.lock().unwrap().len();
			out.push(WasmPoolSnapshot {
				kind: "stateful",
				key: pool.module_id.clone(),
				export: pool.export_name.clone(),
				capacity: pool.capacity,
				available,
				total_allocations: pool.total_allocations.load(std::sync::atomic::Ordering::Relaxed),
				failures: pool.failures.load(std::sync::atomic::Ordering::Relaxed),
			});
		}
		// Stateless pools: capacity / available are zero by design
		// (on-demand instantiation, no warm cache); the counters
		// nonetheless reflect real activity.
		let stateless = self.stateless_pools.read().unwrap();
		for (k, pool) in stateless.iter() {
			out.push(WasmPoolSnapshot {
				kind: "stateless",
				key: k.module_id.clone(),
				export: k.export_name.clone(),
				capacity: 0,
				available: 0,
				total_allocations: pool.total_allocations.load(std::sync::atomic::Ordering::Relaxed),
				failures: pool.failures.load(std::sync::atomic::Ordering::Relaxed),
			});
		}
		out
	}

	/// Re-read `path` from disk, re-compile / deserialize the
	/// component, re-call `get-metadata`, and atomically swap the
	/// runtime's component + metadata maps. Compares the new
	/// metadata against the old to classify the outcome — see
	/// [`ReloadComponentOutcome`].
	///
	/// On `MetadataUnchanged` the matching live stateful pools have
	/// their generation bumped via [`StatefulPoolHandle::swap_component_and_bump`]
	/// so subsequent checkouts pick up the new component without
	/// recompiling the flow graph.
	///
	/// # Errors
	///
	/// Surfaces I/O errors reading the file, wasmtime compile errors,
	/// `get-metadata` traps, or `validate_handler_exports` rejections.
	///
	/// # Panics
	///
	/// Panics if any internal `RwLock` is poisoned.
	pub async fn reload_component(&self, path: &Path) -> Result<ReloadComponentOutcome, Error> {
		let bytes =
			std::fs::read(path).map_err(|e| Error::internal(format!("read {}: {e}", path.display())))?;
		let new_hash = sha256_bytes(&bytes);
		let key = path.to_string_lossy().into_owned();

		// Fast path: identical bytes, nothing to do.
		if let Some(prev_hash) = self.module_hashes.read().unwrap().get(&key)
			&& *prev_hash == new_hash
		{
			return Ok(ReloadComponentOutcome::Unchanged);
		}

		let old_meta = self.metadata.read().unwrap().get(&key).cloned();

		let cwasm_path =
			std::path::PathBuf::from(CWASM_CACHE_DIR).join(format!("{}.cwasm", hex_sha256(&bytes)));
		let component = load_or_compile(&self.engine, path, &cwasm_path, &bytes)?;
		let new_meta = get_metadata(
			&self.engine,
			&component,
			Arc::clone(&self.fetch_backend),
			Arc::clone(&self.cardinality),
		)
		.await?;
		validate_handler_exports(&self.engine, &component, &new_meta.exports)?;

		let new_component = Arc::new(component);
		// `Some(_)` (incompatible) and `None` (first-load) both fall
		// through to `MetadataChanged`; the caller treats them the
		// same — the registry needs to rebuild its export entries.
		let outcome = match old_meta.as_ref() {
			Some(old) if metadata_compatible(old, &new_meta) => ReloadComponentOutcome::MetadataUnchanged,
			_ => ReloadComponentOutcome::MetadataChanged,
		};

		// Swap component + metadata + hash atomically per-table.
		self.components.write().unwrap().insert(key.clone(), Arc::clone(&new_component));
		self.metadata.write().unwrap().insert(key.clone(), Arc::clone(&new_meta));
		self.module_hashes.write().unwrap().insert(key.clone(), new_hash);

		// Drop stateless pool entries for this module so cumulative
		// counters don't leak across reload — spec doesn't require
		// counter continuity. Stateful: bump generation on every
		// matching live pool.
		self.drop_stateless_pools_for_module(&key);
		self.bump_pool_generation_for_module(&key, &new_component);

		Ok(outcome)
	}

	/// Bump generation + swap component on every live stateful pool
	/// whose `module_id` matches `key`. Pre-built old-generation
	/// instances drop; in-flight ones drop at return time.
	fn bump_pool_generation_for_module(&self, key: &str, new_component: &Arc<Component>) {
		let live: Vec<Arc<StatefulPoolHandle>> = {
			let mut registry = self.stateful_pools.write().unwrap();
			registry.retain(|w| w.upgrade().is_some());
			registry.iter().filter_map(Weak::upgrade).filter(|h| h.module_id == key).collect()
		};
		for handle in live {
			handle.swap_component_and_bump(Arc::clone(new_component));
		}
	}

	/// Remove cached stateless pool entries for `module_id`. Called
	/// from [`Self::reload_component`] so per-pool counters don't
	/// outlive the component swap.
	fn drop_stateless_pools_for_module(&self, key: &str) {
		let mut pools = self.stateless_pools.write().unwrap();
		pools.retain(|k, _| k.module_id != key);
	}
}

/// Compare two `PluginMetadata` values for graph-relevance: same
/// `abi_version`, same export set (by name) with matching `kind` /
/// `stateless` / `needs_body` and equivalent `inspects` (order-
/// independent). Top-level `name` and `version` differences are
/// **not** graph-relevant — spec § _Module identity and reload_
/// explicitly permits relabel-only releases without recompile.
#[must_use]
pub fn metadata_compatible(old: &PluginMetadata, new: &PluginMetadata) -> bool {
	use std::collections::BTreeMap;
	if old.abi_version != new.abi_version {
		return false;
	}
	if old.exports.len() != new.exports.len() {
		return false;
	}
	let old_by_name: BTreeMap<&str, &PluginExport> =
		old.exports.iter().map(|e| (e.name.as_str(), e)).collect();
	let new_by_name: BTreeMap<&str, &PluginExport> =
		new.exports.iter().map(|e| (e.name.as_str(), e)).collect();
	if old_by_name.len() != new_by_name.len()
		|| old_by_name.keys().collect::<Vec<_>>() != new_by_name.keys().collect::<Vec<_>>()
	{
		return false;
	}
	for (name, old_e) in &old_by_name {
		let Some(new_e) = new_by_name.get(name) else {
			return false;
		};
		if old_e.kind != new_e.kind
			|| old_e.stateless != new_e.stateless
			|| old_e.needs_body != new_e.needs_body
		{
			return false;
		}
		// inspects is order-independent — compare as sorted vectors.
		let mut a = old_e.inspects.clone();
		let mut b = new_e.inspects.clone();
		a.sort();
		b.sort();
		if a != b {
			return false;
		}
	}
	true
}

/// One entry in [`WasmtimeRuntime::pool_snapshot`]. Mirrors the wire
/// shape exposed by the `get_pools` mgmt verb, but lives in this crate
/// so `vane-mgmt` does not have to depend on `vane-wasm` to consume
/// the data — the daemon translates to the wire shape via the
/// [`WasmPoolStats`] trait.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct WasmPoolSnapshot {
	pub kind: &'static str,
	pub key: String,
	pub export: String,
	pub capacity: usize,
	pub available: usize,
	/// Cumulative successful checkouts (stateful) or rentals
	/// (stateless).
	pub total_allocations: u64,
	/// Cumulative checkout / instantiation failures.
	pub failures: u64,
}

